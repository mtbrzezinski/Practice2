package OOP.Inheritance1;

public class Flower {
    double height;
    String color;
    double price;
    int numberOfPetals;


    public Flower(double height, String color, double price, int numberOfPetals) {
        this.height = height;
        this.color = color;
        this.price = price;
        this.numberOfPetals = numberOfPetals;
    }
    public Flower(){}
}

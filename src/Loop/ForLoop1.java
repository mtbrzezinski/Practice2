package Loop;

public class ForLoop1 {
    public static void main(String[] args) {
        for (int i = 5; i <= 10; i++) {
            System.out.print((i) + ", ");
        }
        int b = 5;
        for (; b <= 10; ) {
            System.out.println(b);
            b++;
        }
        int c = 1;
        for (; c <= 8; c++) {
            System.out.print((c) + ". ");


        }
    }
}

package Loop;

public class DoWhile {
    public static void main(String[] args) {

        int a = 3, b = 5;
        do {
            System.out.println("This is Do While Loop: ");
            a++;
        }while(a<=b);
       a = 3;
       b = 5;
        do {
            System.out.println("This is Do While Loop 2222: ");
            a++;
        }while(a>b);
    }
}

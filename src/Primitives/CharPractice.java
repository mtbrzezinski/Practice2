package Primitives;

public class CharPractice {
    public static void main(String[] args) {

        char letter = 'T'; // values of the char has to be in '' quotation mark
        char letter2 = 'A';
        // char letter2 = "A"; it will not compile

        char letter3 = '9';
        char symbol = '*';
        char symbol1 = ' ';

        System.out.println(letter); // T --> ASCII table value for letter T is 84
        System.out.println(letter3); // 9 ---> ASCII table dec value for 9 is 57
        System.out.println(symbol);
        System.out.println(symbol1);
        System.out.println(symbol);

        int x = 4;
        System.out.println(x);
        int something = 'A'; // A is 65 in ASCII Table
        System.out.println(something);

        char something8 = 99;
        System.out.println(something8);
        char random = 500;
        System.out.println(random);

    }
}

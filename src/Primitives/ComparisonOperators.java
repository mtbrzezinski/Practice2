package Primitives;

public class ComparisonOperators {

    public static void main(String[] args) {
        int a = 7, b = 27;
        boolean result = a ==b;
        System.out.println(result);

        boolean result1 = a != b;
        System.out.println(result1);

        boolean result2 = a < b;
        System.out.println(result2);

        boolean result3 = a > b;
        System.out.println(result3);
    }
}
